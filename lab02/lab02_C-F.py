# El float sirve de conversion de tipo, el usuario ingresa un valor y Python trata de convertirlo a float
temperaturaCelcius = float(input("Cual es la temperatura en Celcius? "))
temperatureFahrenheit = (9/5) * temperaturaCelcius + 32
print(temperatureFahrenheit)